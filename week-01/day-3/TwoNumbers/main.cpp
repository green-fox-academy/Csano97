#include <iostream>

int main() {
    int a = 22;
    int b = 13;
    std::cout << "22 + 13: " << a + b << std::endl;
    std::cout << "22 - 13: " << a - b << std::endl;
    std::cout << "22 * 13: " << a * b << std::endl;
    std::cout << "22 / 13: " << a / b << std::endl;
    std::cout << "22 / 13: " << (float)a / b << std::endl;
    std::cout << "22 % 13: " << a % b << std::endl;
    return 0;
}