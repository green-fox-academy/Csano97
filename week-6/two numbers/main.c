#include <stdio.h>

int main()
{
    int two_numbers_results;
    two_numbers_results = 22 + 13;
    printf("%d\n", two_numbers_results);
    two_numbers_results = 22 - 13;
    printf("%d\n", two_numbers_results);
    two_numbers_results = 22 * 13;
    printf("%d\n", two_numbers_results);
    float two_numbers_float =  22.0 / 13.0;
    printf("%.2f\n", two_numbers_float);
    return 0;
}